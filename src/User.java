public class User {
    private int id;
    private String name;
    private String cpf;
    private String email;

    public int getId(){
        return this.id;
    }
    public String getName(){
        return this.name;
    }
    public String getCpf(){
        return this.cpf;
    }
    public String getEmail(){
        return this.email;
    }
}
